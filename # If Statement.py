# If Statement

RespuestaUsuario = input('¿Necesitas Enviar Un Paquete? (ingrese yes, no o ninguno)')

if RespuestaUsuario == 'yes':
    print('Te podemos ayudar a cambiar este paquete!!!')
elif RespuestaUsuario == 'no':
    print('Haga una orden acontinuación para ser atendido')
else:
    print('Gracias por comunicarse, adios!!!')

