# Datos numericos suma
print('Python has three numeric types: int, float, and complex')

myValue = 1
print(myValue)

print(type(myValue))

print(str(myValue)) + 'is of the data type' + print(str(myValue))

myValue = 3.14
print(myValue)

print(type(myValue))

print(str(myValue)) + 'is of the data type' + print(str(myValue))

myValue = 6j
print(myValue)

print(type(myValue))

print(str(myValue)) + 'is of the data type' + print(str(myValue))

myValue = True
print(myValue)

print(type(myValue))

print(str(myValue)) + 'is of the data type' + print(str(myValue))

myValue = False
print(myValue)

print(type(myValue))

print(str(myValue)) + 'is of the data type' + print(str(myValue))


