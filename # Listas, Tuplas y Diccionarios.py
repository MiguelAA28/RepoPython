# Listas, Tuplas y Diccionarios

mylistacompleta = ['manzana', 'banano', 'cereza']

print(mylistacompleta)
print(type(mylistacompleta))
print(mylistacompleta[0])
print(mylistacompleta[1])
print(mylistacompleta[2])

mylistacompleta[2] = 'fresa'
print('mylistacompleta')


myTuplaFinal = ('manzana', 'banano', 'pera')
print(myTuplaFinal)
print(type(myTuplaFinal))
print(myTuplaFinal[0])
print(myTuplaFinal[1])
print(myTuplaFinal[2])

mydiccionario = {'aio' : 'banano', 'mdf' : 'manzana', 'los' : 'pera'}
print(mydiccionario)
print(type(mydiccionario))
print(mydiccionario['aio'])
print(mydiccionario['mdf'])
print(mydiccionario['los'])